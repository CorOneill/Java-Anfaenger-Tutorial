package javatutorial.kapitel5.listing23;

public class IncrementAndDecrement {
    public static void main(String[] args) {
        //Variable number wird mit 5 initialisiert.
        int number = 5;
        //Variable number wird inkrementiert.
        number++;
        //Ausgabe: "Nummer: 6"
        System.out.println("Nummer: " + number);
        //Variable number wird dekrementiert.
        number--;
        //Ausgabe: "Nummer: 5"
        System.out.println("Nummer: " + number);
    }
}